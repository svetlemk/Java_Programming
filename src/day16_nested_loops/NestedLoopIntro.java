package day16_nested_loops;

public class NestedLoopIntro {

    public static void main(String[] args) {


        for (int i = 0; i < 5; i++) {
            System.out.println("Wooden Spoon");
        }

        System.out.println("-----------------");
        for (int i = 0; i < 5; i++) {

            for (int j = 0; j < 6; j++) {
               // System.out.println("---------");
                System.out.println("hello");

            }

        }







    }
}
