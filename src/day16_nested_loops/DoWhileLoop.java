package day16_nested_loops;

public class DoWhileLoop {
    public static void main(String[] args) {

        boolean condition = false;

        for (; condition ; ){
            System.out.println("Hello Cydeo ---- for loop");

        }

        System.out.println("-----------");

        while (condition){
            System.out.println("hello cydeo ======= while");
        }

        System.out.println("-------------------");

        do{
            System.out.println("hello cydeo do while loop");//loop body sout

        }while(condition);



    }
}
