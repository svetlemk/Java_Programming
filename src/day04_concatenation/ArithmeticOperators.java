package day04_concatenation;

public class ArithmeticOperators {
    public static void main(String[] args) {
        System.out.println(343 + 444);
        System.out.println(4490 - 278);
        System.out.println(50 / 6);
        System.out.println(50.0 / 6);
        System.out.println(50d / 6);

        System.out.println( (int)(2.5 / 0.5) );
        //System.out.println(9 / 0); 0 not allowed
        System.out.println(10 * 12);
        //modulus
        System.out.println("----------------");
        System.out.println(10 % 3);
        System.out.println(45 % 13);

    }
}
